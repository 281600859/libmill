/* Event handler for coroutine 'tcpsocket_accept'. */
static int mill_handler_tcpsocket_accept (void *cfptr, void *event)
{
    struct mill_cf_tcpsocket_accept *cf;

    cf = (struct mill_cf_tcpsocket_accept*) cfptr;

    /* Continue from the point where the coroutine was interrupted. */
    switch (cf->mill_cfh.pc) {
        case 0: break;
        case 1: goto mill_pc_1;
        case 2: goto mill_pc_2;
        case 3: goto mill_pc_3;
        default: assert (0);
    }

    if ((cf->self)->state != TCPSOCKET_STATE_INIT ||
          (cf->from)->state != TCPSOCKET_STATE_LISTENING ||
          (cf->from)->recvcfptr != NULL) {
        *(&cf->rc) = EFSM;
        goto mill_finally;
    }
    
    while (1) {
    
        *(&cf->rc) = uv_accept ((uv_stream_t*) &(cf->from)->s, (uv_stream_t*) &(cf->self)->s);
        /* TODO: Handle errors. */
        if (*(&cf->rc) == 0)
           break;
    
        /* Link the lisening socket with the accepting socket and wait
           for incoming connection. */
        (cf->from)->recvcfptr = cf;
        (cf->self)->state = TCPSOCKET_STATE_ACCEPTING;
    
        /* Wait for an incoming connection. */
        cf->mill_cfh.pc = 1;
        return 0;
        mill_pc_1:;
        if (!event) {
            (cf->from)->recvcfptr = NULL;
            (cf->self)->state = TCPSOCKET_STATE_INIT;
            goto mill_finally;
        }
    
        /* There's a new incoming connection. Let's accept it. */
        assert (event == tcpsocket_listen_cb);
    }
    
    /* Connection was successfully accepted. */
    (cf->self)->state = TCPSOCKET_STATE_ACTIVE;

/* The main part of the coroutine is finished.
   What follows is the termination-related code. */
mill_finally:

    /* Cancel any remaining child coroutines. */
    mill_cancel_children (cf);
    cf->mill_cfh.pc = 2;
    goto mill_test_children;
    mill_pc_2:
    mill_coframe_term (event, 1);

    /* If there are no remaining children notify the parent that
       the coroutine have finished. */
    mill_test_children:
    if (mill_has_children (cf))
        return 0;
    mill_emit (cf);
    cf->mill_cfh.pc = 3;
    return 0;
    mill_pc_3:

    /* If finished coroutine was selected
       copy the out arguments to their final destinations. */
    if (event) {
        assert (event == cf);
        if (cf->mill_out_rc)
            *cf->mill_out_rc = cf->rc;
        cf->mill_cfh.pc = -1;
        return 0;
    }

    cf->mill_cfh.pc = -1;
    return 0;
}

